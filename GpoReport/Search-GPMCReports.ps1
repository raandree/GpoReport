    )
    
    $details = @{
        Name = "Unknown"
        State = "Unknown" 
        Value = $null
        Action = $null
        Type = $null
        Context = $null
        Comment = $null
    }
    
    $currentNode = $Node
    $searchDepth = 0
    
    # Look for setting properties in the current node and nearby context
    while ($null -ne $currentNode -and $searchDepth -lt 15) {
        if ($currentNode.NodeType -eq [System.Xml.XmlNodeType]::Element) {
            
            # Check current node attributes
            $attributes = @{}
            foreach ($attr in $currentNode.Attributes) {
                $attributes[$attr.Name] = $attr.Value
            }
            
            # Extract meaningful attributes
            if ($attributes.ContainsKey("name") -and $details.Name -eq "Unknown") {
                $details.Name = $attributes["name"]
            }
            if ($attributes.ContainsKey("status") -and $details.State -eq "Unknown") {
                $details.State = $attributes["status"]
