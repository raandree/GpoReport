try {
    # Convert search string to regex pattern
    $regexPattern = ConvertTo-RegexPattern -WildcardPattern $SearchString -CaseSensitive $CaseSensitive.IsPresent
    Write-Verbose "Using regex pattern: $regexPattern"
    
    # Search through all content based on parameter set
    $allResults = @()
    
    if ($PSCmdlet.ParameterSetName -eq 'FilePath') {
        # Handle file/directory path
        $pathItem = Get-Item -Path $Path
        $xmlFiles = @()
        
        if ($pathItem.PSIsContainer) {
            # Directory - find all XML files
            $searchParams = @{
                Path = $Path
                Filter = "*.xml"
                File = $true
            }
            if ($Recurse) {
                $searchParams.Recurse = $true
            }
            
            $xmlFiles = Get-ChildItem @searchParams | Select-Object -ExpandProperty FullName
            
            if ($xmlFiles.Count -eq 0) {
                Write-Warning "No XML files found in directory: $Path"
                return
            }
            
            Write-Host "Found $($xmlFiles.Count) XML files to search" -ForegroundColor Cyan
        }
        else {
            # Single file
            $xmlFiles = @($pathItem.FullName)
        }
        
        # Search through all files
        foreach ($xmlFile in $xmlFiles) {
            $fileResults = Search-GPMCXmlFile -FilePath $xmlFile -RegexPattern $regexPattern -IncludeAllMatches $IncludeAllMatches.IsPresent -MaxResults $MaxResults
            $allResults += $fileResults
            
            if ($MaxResults -gt 0 -and $allResults.Count -ge $MaxResults) {
                $allResults = $allResults | Select-Object -First $MaxResults
                break
            }
        }
        
        # Display source information
        Write-Host "`n=== GPMC SEARCH RESULTS ===" -ForegroundColor Cyan
        Write-Host "Search Pattern: $SearchString" -ForegroundColor Yellow
        if ($xmlFiles.Count -eq 1) {
            Write-Host "File: $($xmlFiles[0])" -ForegroundColor Yellow
        } else {
            Write-Host "Files: $($xmlFiles.Count) XML files searched" -ForegroundColor Yellow
        }
    }
    elseif ($PSCmdlet.ParameterSetName -eq 'XmlContent') {
        # Handle XML content array
        Write-Host "Found $($XmlContent.Count) XML content strings to search" -ForegroundColor Cyan
        
        # Search through all XML content strings
        for ($i = 0; $i -lt $XmlContent.Count; $i++) {
            $xmlString = $XmlContent[$i]
            $sourceId = "XML Content [$($i + 1)]"
            
            $contentResults = Search-GPMCXmlString -XmlContentString $xmlString -RegexPattern $regexPattern -IncludeAllMatches $IncludeAllMatches.IsPresent -MaxResults $MaxResults -SourceIdentifier $sourceId
            $allResults += $contentResults
            
            if ($MaxResults -gt 0 -and $allResults.Count -ge $MaxResults) {
                $allResults = $allResults | Select-Object -First $MaxResults
                break
            }
        }
        
        # Display source information
        Write-Host "`n=== GPMC SEARCH RESULTS ===" -ForegroundColor Cyan
        Write-Host "Search Pattern: $SearchString" -ForegroundColor Yellow
        Write-Host "Source: $($XmlContent.Count) XML content string(s)" -ForegroundColor Yellow
    }
    
    Write-Host "Search completed. Found $($allResults.Count) matches." -ForegroundColor Green
    
    if ($allResults.Count -eq 0) {
        Write-Warning "No matches found for pattern: $SearchString"
        return
    }
    Write-Host ("-" * 80) -ForegroundColor Gray
    
    $resultNumber = 1
    foreach ($result in $allResults) {
        Write-Host "`n[$resultNumber] MATCH FOUND" -ForegroundColor Green
        Write-Host "Matched Text: " -NoNewline -ForegroundColor White
        Write-Host "$($result.MatchedText)" -ForegroundColor Yellow
        Write-Host "Match Type: " -NoNewline -ForegroundColor White
        Write-Host "$($result.MatchType)" -ForegroundColor Cyan
        
        Write-Host "`nGPO Details:" -ForegroundColor Cyan
        Write-Host "  Display Name: $($result.GPO.DisplayName)" -ForegroundColor White
        Write-Host "  Domain: $($result.GPO.DomainName)" -ForegroundColor White
        Write-Host "  GUID: $($result.GPO.GUID)" -ForegroundColor White
        Write-Host "  Section: $($result.Section)" -ForegroundColor White
        if ($result.GPO.CreatedTime) {
            Write-Host "  Created: $($result.GPO.CreatedTime)" -ForegroundColor White
        }
        if ($result.GPO.ModifiedTime) {
            Write-Host "  Modified: $($result.GPO.ModifiedTime)" -ForegroundColor White
        }
        
        Write-Host "`nSetting Details:" -ForegroundColor Cyan
        Write-Host "  Name: $($result.Setting.Name)" -ForegroundColor White
        Write-Host "  State: $($result.Setting.State)" -ForegroundColor White
        if ($result.Setting.Value) {
            Write-Host "  Value: $($result.Setting.Value)" -ForegroundColor White
        }
        if ($result.Setting.Action) {
            Write-Host "  Action: $($result.Setting.Action)" -ForegroundColor White
        }
        if ($result.Setting.Type) {
            Write-Host "  Type: $($result.Setting.Type)" -ForegroundColor White
        }
        if ($result.Setting.Context) {
            Write-Host "  Context: $($result.Setting.Context)" -ForegroundColor White
        }
        if ($result.Comment) {
            Write-Host "  Comment: $($result.Comment)" -ForegroundColor Yellow
        }
        
        if ($result.CategoryPath -ne "Unknown") {
            Write-Host "`nCategory Path: $($result.CategoryPath)" -ForegroundColor Magenta
        }
        
        if ($xmlFiles.Count -gt 1) {
            Write-Host "`nSource File: $([System.IO.Path]::GetFileName($result.SourceFile))" -ForegroundColor Gray
        }
        
        Write-Host ("-" * 80) -ForegroundColor Gray
        $resultNumber++
    }
    
    # Also return the objects for further processing
    return $allResults
}
catch {
    Write-Error "Error during search: $($_.Exception.Message)"
    throw
}
